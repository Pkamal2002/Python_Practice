# Using only comparison operators (==, !=, <, >, <=, >=), write a program that takes two numbers as
# input and prints the result of each comparison as True or False.

num1 = int(input("Enter the First Number:"))
num2 = int(input("Enter the Second Number:"))

# Comparison operators

print("Both Num are equals: " ,(num1==num2))
print("Both Num are Not equals: " ,(num1!=num2))
print("Num1 is Greater: " ,(num1>num2))
print("Num1 is less: " ,(num1<num2))
print("Num1 is less then or equal: " ,(num1<=num2))
print("Num1 is greater then or equal: " ,(num1>=num2))